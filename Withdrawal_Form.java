    public void seeDatabase() {
        Object[] row = {"Withdrawed By", "Type", "Transaction ID", "From", "To", "Amount", "Estimate"};
        tabmode = new DefaultTableModel(null, row);
        tables.setModel(tabmode);
        try {
            result = cc.seeData();
            while (result.next()) {
                cc.setWithdrawedBy(result.getString("withdrawed_by"));
                cc.setType(result.getString("crypto_type"));
                cc.setTransactionID(result.getString("id"));
                cc.setReceiveFrom(result.getString("withdraw_to"));
                cc.setSendTo(result.getString("received_from"));
                cc.setAmount(result.getString("amount"));
                cc.setEstimate(result.getString("estimate"));
                String[] data = {cc.getWithdrawedBy(), cc.getType(), cc.getTransactionID(), cc.getReceiveFrom(), cc.getSendTo(), cc.getAmount(), cc.getEstimate()};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
        }
    }

